// Declares the setup function
function setup() {

  //Creates a canvas of size specified in parentheses
  createCanvas( 600, 400 );

  //Sets background color to color specified
  background( 'green' );
}
//Declares a function named 'draw'
function draw() {

  /* Creates the text 'Hello World!' on the page.
     This text gets aligned to the x and y axis
     at x: 20, y: 30 */
  text( "Hello World!", 20, 30 );

}
